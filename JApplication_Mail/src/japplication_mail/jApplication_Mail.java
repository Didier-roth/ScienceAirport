    private void lst_mailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lst_mailsMouseClicked
        // TODO add your handling code here:
        JList<String> theList = (JList) evt.getSource();
        if (evt.getClickCount() == 2) 
        {
          int index = theList.locationToIndex(evt.getPoint());
          if (index >= 0) {
            Object o = theList.getModel().getElementAt(index);
            System.out.println("Double-clicked on: " + o.toString());
            JOptionPane.showMessageDialog(this,((Mail)o).getPrintableText(),((Mail)o).getSujet(),JOptionPane.NO_OPTION);
          }
        }
        else
        {
            if (SwingUtilities.isRightMouseButton(evt)) 
            {
              int index = theList.locationToIndex(evt.getPoint());
              if (index >= 0) {
                Object o = theList.getModel().getElementAt(index);
                System.out.println("right-clicked on: " + o.toString());
                JOptionPane.showMessageDialog(this,((Mail)o).getPrintableHeader(),((Mail)o).getSujet(),JOptionPane.NO_OPTION);
              }
            }
        }
    }//GEN-LAST:event_lst_mailsMouseClicked

