    private void btn_choisirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_choisirActionPerformed
        // TODO add your handling code here:
         JFileChooser chooser = new JFileChooser();
         chooser.setMultiSelectionEnabled(true);
        // Note: source for ExampleFileFilter can be found in FileChooserDemo,
        // under the demo/jfc directory in the JDK.
        int returnVal;
        returnVal = chooser.showOpenDialog(this);
        if(returnVal == JFileChooser.APPROVE_OPTION) 
        {
            
            String fnm = "";
            for(File f : chooser.getSelectedFiles())
            {
                System.out.println("You chose to open this file: " + f.getPath());
                fnm = fnm + f.getPath() + ";";
            }
            this.piecejointe = chooser.getSelectedFiles();
            this.txtf_image.setText(fnm);
        }
    }//GEN-LAST:event_btn_choisirActionPerformed

    private void butSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butSendActionPerformed
        Properties prop = System.getProperties(); 

        prop.put("mail.smtp.auth", Boolean.toString(param.getAuthentification()));
        prop.put("mail.smtp.starttls.enable", Boolean.toString(param.getTtls()));
        prop.put("mail.smtp.host", param.getSmtpServer());
        prop.put("mail.smtp.port", param.getSmtpPort());
        
        
      
        if(prop.get("mail.smtp.host").equals("smtp.gmail.com"))
        {
            prop.setProperty("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory"); 
            prop.setProperty("mail.smtp.socketFactory.fallback", "false"); 
            prop.setProperty("mail.smtp.port", "465"); 
            prop.setProperty("mail.smtp.socketFactory.port", "465"); 
        }
        
        if (param.getAuthentification()) 
        {
            
            sess = Session.getInstance(prop, new javax.mail.Authenticator() 
            {
                protected PasswordAuthentication getPasswordAuthentication() 
                {
                    return new PasswordAuthentication(param.getEmail(), param.getPassword());
                }
            });
        } 
        else 
        {
            sess = Session.getDefaultInstance(prop);
        }
       
        
        
        if(this.txtf_image.getText().isEmpty())
        {
           try 
           {
                System.out.println("Création du message texte");
                MimeMessage msg = new MimeMessage(sess);
                if(!param.getAuthentification())
                    msg.setFrom(new InternetAddress(this.Txtf_exp.getText()));
                else
                    msg.setFrom(new InternetAddress(param.getEmail()));
                    
                msg.setRecipient(Message.RecipientType.TO, new InternetAddress(this.Txtf_dest.getText()));
                msg.setSubject(this.Txtf_obj.getText());

                String texte = this.Txta_txt.getText();

                msg.setText (texte);

                System.out.println("Envoi du message");
                Transport.send(msg); 
                System.out.println("Message envoyé");
           } 
           catch (AddressException ex) 
            {
                Logger.getLogger(jApplication_Mail.class.getName()).log(Level.SEVERE, null, ex);
            } 
            catch (MessagingException ex) 
            {
                Logger.getLogger(jApplication_Mail.class.getName()).log(Level.SEVERE, null, ex);
            }
       }
       else
       {
            try {
                MimeMessage msg = new MimeMessage (sess);
                
                if(!param.getAuthentification())
                    msg.setFrom(new InternetAddress(this.Txtf_exp.getText()));
                else
                    msg.setFrom(new InternetAddress(param.getEmail()));
                
                msg.setRecipient(Message.RecipientType.TO, new InternetAddress(this.Txtf_dest.getText()));
                msg.setSubject(this.Txtf_obj.getText());
                
                System.out.println("Début construction du multipart"); 
                
                Multipart msgMP = new MimeMultipart();
                // 1ère composante : le texte d'accompagnement
                System.out.println("1ère composante");
                
                MimeBodyPart msgBP = new MimeBodyPart();
                msgBP.setText(this.Txta_txt.getText());
                msgMP.addBodyPart(msgBP);
                
                
                System.out.println("Piece jointe");
                
                for (File f : piecejointe)
                {
                    System.out.println("ajout d'une piece jointe");
                    String nf = f.getPath(); 
                    msgBP = new MimeBodyPart();

                    DataSource so = new FileDataSource (nf); 
                    msgBP.setDataHandler (new DataHandler (so)); 
                    
                    
                    msgBP .setFileName(f.getName()); 
                    msgMP.addBodyPart(msgBP);
                }
                
                msg.setContent(msgMP); 
                System.out.println("Envoi du message"); 
                Transport.send(msg); 
                System.out.println("Message envoyé");
                
            } catch (AddressException ex) {
                Logger.getLogger(jApplication_Mail.class.getName()).log(Level.SEVERE, null, ex);
            } catch (MessagingException ex) {
                Logger.getLogger(jApplication_Mail.class.getName()).log(Level.SEVERE, null, ex);
            }
       }
    }//GEN-LAST:event_butSendActionPerformed

